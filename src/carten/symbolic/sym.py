"""
Symmetrization of generic tensor to get tensor with specified symmetry.
"""

import numpy as np


def generate_closure(generators: list[tuple[int, ...]]) -> list[tuple[int, ...]]:
    """
    Generate the closure of a set of permutations.

    Args:
        generators: A list of tuples representing the generator permutations. Each tuple
            is a permutation of indices.

    Returns:
        A list of all permutations generated by the input generators.
    """
    if not generators:
        return []
    seen = set(generators)
    queue = list(generators)

    while queue:
        p = queue.pop(0)
        for g in generators:
            # Compute g ∘ p: apply permutation g to the result of permutation p
            composed1 = tuple(g[i] for i in p)
            if composed1 not in seen:
                seen.add(composed1)
                queue.append(composed1)
            # Compute p ∘ g: apply permutation p to the result of permutation g
            composed2 = tuple(p[i] for i in g)
            if composed2 not in seen:
                seen.add(composed2)
                queue.append(composed2)

    return list(seen)


